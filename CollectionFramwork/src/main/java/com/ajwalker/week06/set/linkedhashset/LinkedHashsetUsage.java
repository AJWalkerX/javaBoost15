package com.ajwalker.week06.set.linkedhashset;

import java.util.LinkedHashSet;

public class LinkedHashsetUsage {
	public static void main(String[] args) {
		LinkedHashSet<String> sehirler = new LinkedHashSet<>();
		sehirler.add("A");
		sehirler.add("D");
		sehirler.add("v");
		sehirler.add("C");
		sehirler.add("E");
		
		System.out.println(sehirler);
	}
}